package taojava.lab.codereading;

/**
 * A simple collection of mathematical routines.
 * 
 * @author Samuel A. Rebelsky
 */
public class MathUtils
{
  /**
   * Square an integer.
   */
  public static int
    square (int x)
  {
    return x * x;
  } // square(int)

  /**
   * Square a double.
   */
  public static double
    square (double x)
  {
    return x * x;
  } // square(double)
} // class MathUtils
